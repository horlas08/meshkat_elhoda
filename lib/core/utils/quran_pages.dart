  static ({int surahNumber, String surahName}) getSurahDataForPage(int page) {
    int surahNumber = 1;
    for (var entry in surahStartPages.entries) {
      if (entry.value <= page) {
        surahNumber = entry.key;
      } else {
        break;
      }
    }
    return (surahNumber: surahNumber, surahName: surahNames[surahNumber] ?? '');
  }

  static int getJuzForPage(int page) {
    // Standard Madani Mushaf approximation
    return ((page - 1) ~/ 20) + 1;
  }

  static int getHizbForPage(int page) {
    // Standard Madani Mushaf approximation
    return ((page - 1) ~/ 10) + 1;
  }
